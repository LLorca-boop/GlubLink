/// Токены радиусов скругления приложения GlubLink
class AppRadius {
  AppRadius._();

  /// Минимальное скругление: 4px
  static const double xs = 4.0;

  /// Малое скругление (кнопки): 8px
  static const double sm = 8.0;

  /// Среднее скругление (карточки): 12px
  static const double md = 12.0;

  /// Большое скругление: 16px
  static const double lg = 16.0;

  /// Максимальное скругление: 24px
  static const double xl = 24.0;

  /// Полное скругление (для круглых элементов)
  static const double full = 9999.0;
}
